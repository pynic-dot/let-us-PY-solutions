# This code is the use of  math modules trunc and floor and ceil
import math
x = -2.8, -0.5, 0.2, 1.5, 2.9
for a in x:
    print("The trunc of {}  is :{}".format(a, math.trunc(a)))
    print("The floor of {}  is :{}".format(a, math.floor(a)))
    print("The ceil of {}  is :{}\n".format(a, math.ceil(a)))
